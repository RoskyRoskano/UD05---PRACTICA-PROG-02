def suma_enteros(a, b, c=0, d=0, e=0):
    return a + b + c + d + e

# Ejemplo de la funci√≥n con diferentes cantidades de enteros
resultado_2_enteros = suma_enteros(3, 5)
print("Suma de 2 enteros:", resultado_2_enteros)

resultado_3_enteros = suma_enteros(2, 4, 6)
print("Suma de 3 enteros:", resultado_3_enteros)

resultado_4_enteros = suma_enteros(1, 2, 3, 4)
print("Suma de 4 enteros:", resultado_4_enteros)

resultado_5_enteros = suma_enteros(5, 10, 15, 20, 25)
print("Suma de 5 enteros:", resultado_5_enteros)
